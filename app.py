# --- Fungsi membuat file Word (DIPERBAIKI) ---
def create_word_document(content, topic_name):
    doc = Document()
    
    for section in doc.sections:
        section.top_margin = Inches(1)
        section.bottom_margin = Inches(1)
        section.left_margin = Inches(1)
        section.right_margin = Inches(1)
    
    title = doc.add_heading('RENCANA PELAKSANAAN PEMBELAJARAN (RPP)', level=1)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    
    doc.add_paragraph(f"Topik: {topic_name}")
    doc.add_paragraph('_' * 50)
    
    lines = content.split('\n')
    for line in lines:
        line = line.strip()
        if not line:
            doc.add_paragraph()
            continue
        
        # Hapus semua markdown bold (**teks**)
        line = re.sub(r'\*\*([^*]+)\*\*', r'\1', line)
        
        # Heading level 2 (##)
        if line.startswith('##'):
            heading_text = line.lstrip('#').strip()
            doc.add_heading(heading_text, level=2)
            continue
        
        # Heading level 3 (###)
        if line.startswith('###'):
            heading_text = line.lstrip('#').strip()
            doc.add_heading(heading_text, level=3)
            continue
        
        # List bullet dengan - atau *
        if line.startswith('-') or line.startswith('*'):
            bullet_text = line[1:].strip()
            doc.add_paragraph(bullet_text, style='List Bullet')
            continue
        
        # Paragraf biasa
        doc.add_paragraph(line)
    
    from datetime import datetime
    footer = doc.sections[0].footer
    footer_para = footer.paragraphs[0] if footer.paragraphs else footer.add_paragraph()
    footer_para.text = f"Generated by AI Lesson Plan Generator | {datetime.now().strftime('%d/%m/%Y %H:%M')}"
    footer_para.style = doc.styles['Normal']
    footer_para.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.CENTER
    
    doc_bytes = BytesIO()
    doc.save(doc_bytes)
    doc_bytes.seek(0)
    return doc_bytes

# --- Fungsi panggil Gemini dengan System Instruction ---
def call_gemini(prompt_text, language):
    if language == "Indonesian":
        system_instruction = "Anda adalah asisten guru yang hanya merespon dalam Bahasa Indonesia. Anda tidak pernah menggunakan Bahasa Inggris sama sekali. Semua judul, deskripsi, dan konten harus dalam Bahasa Indonesia murni."
    else:
        system_instruction = "You are a teacher assistant who only responds in English. You never use Indonesian language at all. All headings, descriptions, and content must be in pure English."
    
    model = genai.GenerativeModel(
        MODEL_NAME,
        system_instruction=system_instruction
    )
    
    for attempt in range(3):
        try:
            response = model.generate_content(
                prompt_text,
                generation_config={
                    "temperature": 0.7,
                    "max_output_tokens": 6000,
                }
            )
            content = response.text
            content = re.sub(r'^```\w*\n?', '', content)
            content = re.sub(r'\n?```$', '', content)
            return content.strip()
        except Exception as e:
            if attempt < 2 and ("503" in str(e) or "high demand" in str(e).lower()):
                time.sleep(2)
                continue
            st.error(f"Error: {str(e)}")
            return None

